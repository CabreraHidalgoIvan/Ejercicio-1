"""Se piden los números para calcular la distancia entre ellos """
print("Calcularemos la distancia entre 2 números dados, para ello introduce los números")
x = float(input("Número 1 = "))
y = float(input("Número 2 = "))

"""Se calcula la distancia entre ambos números"""
dist = abs(y -x)

"""Se muestra la distancia entre los dos números"""
print(f"La distancia entre x e y es de {dist}")